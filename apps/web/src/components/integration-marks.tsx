export function IntegrationMark({
  name,
  className,
  size = 24,
  brand = false,
  title,
}: {
  name: MarkId;
  className?: string;
  size?: number;
  /** Use the canonical brand color when available. */
  brand?: boolean;
  title?: string;
}) {
  const spec = MARKS[name];
  const fill = brand && spec.brand ? spec.brand : "currentColor";
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={fill}
      className={className}
      role="img"
      aria-label={title ?? spec.label}
    >
      <path d={spec.d} />
      {spec.shadow ? <path d={spec.shadow} opacity={0.55} /> : null}
    </svg>
  );
}

export function IntegrationMarkTile({
  name,
  size = 56,
  className,
  brand = false,
}: {
  name: MarkId;
  size?: number;
  className?: string;
  brand?: boolean;
}) {
  return (
    <div
      className={cn(
        "flex shrink-0 items-center justify-center rounded-xl border border-border bg-surface-2 text-ink",
        className,
      )}
      style={{ width: size, height: size }}
    >
      <IntegrationMark name={name} size={Math.round(size * 0.55)} brand={brand} />
    </div>
  );
}

/** Map a Platform id from the registry to a brand mark, when one exists. */
export function platformMark(platform: string): MarkId | null {
  switch (platform) {
    case "claude-code":
    case "claude-desktop":
      return "claude";
    case "cursor":
      return "cursor";
    case "vscode":
      return "vscode";
    case "windsurf":
      return "windsurf";
    case "codex":
      return "openai";
    case "gemini":
      return "gemini";
    case "raycast":
      return "raycast";
    case "aider":
      return "aider";
    case "zed":
      return "zed";
    case "continue":
      return "continue";
    default:
      return null;
  }
}
